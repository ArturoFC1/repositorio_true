/**
 * Algunas de la variables de JavaScript son las siguientes
 */
//VAR                  Este tipo de variable ya no se recomienda ya que se puede alterar facil la variable
var perroSalvaje = "Coki";
console.log(perroSalvaje);

perroSalvaje = "Negro";
console.log(perroSalvaje);
//LET                  Este tipo de variable es recomendado, se puede alterar la variable pero solo dentro del mismo bloque
let perroTierno = "Kiara";
console.log(perroTierno);

perroTierno = "Cafe";
console.log(perroTierno);
//CONST                Este tipo de varibale no puede alterar la variable
const perroAgresivo = "peluchin";
console.log(perroAgresivo);

